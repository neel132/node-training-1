// Custom module definition
exports.add = (a, b) => {
  return a + b; // Add the numbers.
};

exports.sub = (a, b) => {
  return a - b; // Subtract the numbers.
};

exports.mul = (a, b) => {
  return a * b; // Multiply the numbers.
};

exports.div = (a, b) => {
  return a / b; // Divide the numbers.
};